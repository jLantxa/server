#include "util/CryptoUtils.hpp"

namespace server::crypto {

void sha256(std::string str, Sha256Hash hash) {
    SHA256_CTX sha256;
    SHA256_Init(&sha256);
    SHA256_Update(&sha256, str.c_str(), str.size());
    SHA256_Final(hash, &sha256);
}

}  // namespace server::crypto
